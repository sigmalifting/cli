const getPercentage = (rpe, reps) => {
  const percentage = RPE_CHART_DATA[String(rpe)]?.[String(reps)];
  return typeof percentage === "number" ? percentage : null;
};

export const calculate1RM = (weight, reps, rpe) => {
  if (!Number.isFinite(weight) || weight <= 0) {
    return -1;
  }

  if (!Number.isFinite(reps) || reps < 1) {
    return -1;
  }

  if (!Number.isFinite(rpe)) {
    return -1;
  }

  const percentage = getPercentage(rpe, reps);
  if (!percentage || percentage <= 0) {
    return -1;
  }

  return weight / percentage;
};

export const calculateTrainingWeight = (oneRm, reps, targetRpe) => {
  if (!Number.isFinite(oneRm) || oneRm <= 0) {
    return -1;
  }

  if (!Number.isFinite(reps) || reps < 1) {
    return -1;
  }

  if (!Number.isFinite(targetRpe)) {
    return -1;
  }

  const percentage = getPercentage(targetRpe, reps);
  if (!percentage || percentage <= 0) {
    return -1;
  }

  return oneRm * percentage;
};

export const calculateSuggestedWeightRange = (oneRm, reps, targetRpe) => {
  if (!Number.isFinite(oneRm) || oneRm <= 0) {
    return { minWeight: -1, maxWeight: -1 };
  }

  const minWeight = calculateTrainingWeight(oneRm, reps, targetRpe - 1);
  const maxWeight = calculateTrainingWeight(oneRm, reps, targetRpe + 1);

  if (
    minWeight !== -1 &&
    maxWeight !== -1 &&
    minWeight > maxWeight
  ) {
    return {
      minWeight: maxWeight,
      maxWeight: minWeight,
    };
  }

  return { minWeight, maxWeight };
};
